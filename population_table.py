from math import exp

B = 50000.0     #defines numbers
c = 9 #konstant
k = 0.2 #faktor
periode = 48 # tid
t = [x for x in range(0,periode+1)] #lager liste
n= [B/(1+c*(exp(-1*k*t))) for t in t] #formel for population

for z in zip(t,n):
    print("år{:^5}-->{:^12}".format(z[0],round(z[1],2))) #printer ut liste

